// Transparent option
export const TRANSPARENT = 'TRANSPARENT'

// Helper function to get color display
export function getColorDisplay(color) {
  if (color === TRANSPARENT) {
    return { code: 'EMPTY', name: 'Transparent', hex: null, isTransparent: true }
  }

  const dmcColor = allDMCColors.find(c => c.hex === color)
  if (dmcColor) {
    return { ...dmcColor, isTransparent: false }
  }

  return { code: 'CUSTOM', name: 'Custom', hex: color, isTransparent: false }
}

// Helper to check if color is transparent
export function isTransparent(color) {
  return color === TRANSPARENT || color === null || color === undefined
}

/**
 * Convert hex color to RGB object
 * @param {string} hex - Hex color string (e.g., "#FF0000")
 * @returns {{r: number, g: number, b: number}}
 */
function hexToRgb(hex) {
  // Remove # if present
  hex = hex.replace(/^#/, '')

  // Parse hex to RGB
  const r = parseInt(hex.substring(0, 2), 16)
  const g = parseInt(hex.substring(2, 4), 16)
  const b = parseInt(hex.substring(4, 6), 16)

  return { r, g, b }
}

/**
 * Calculate Euclidean distance between two RGB colors
 * @param {{r: number, g: number, b: number}} rgb1
 * @param {{r: number, g: number, b: number}} rgb2
 * @returns {number}
 */
function colorDistance(rgb1, rgb2) {
  const rDiff = rgb1.r - rgb2.r
  const gDiff = rgb1.g - rgb2.g
  const bDiff = rgb1.b - rgb2.b

  return Math.sqrt(rDiff * rDiff + gDiff * gDiff + bDiff * bDiff)
}

/**
 * Find the closest DMC color to a given hex color
 * @param {string} hexColor - The input color in hex format (e.g., "#FF5733")
 * @returns {string} The hex value of the closest DMC color
 */
export function findClosestDMCColor(hexColor) {
  // Handle transparent or invalid colors
  if (!hexColor || isTransparent(hexColor)) {
    return TRANSPARENT
  }

  // Ensure hex format
  if (!hexColor.startsWith('#')) {
    hexColor = '#' + hexColor
  }

  const inputRgb = hexToRgb(hexColor)

  let closestColor = allDMCColors[0]
  let minDistance = Infinity

  for (const dmcColor of allDMCColors) {
    const dmcRgb = hexToRgb(dmcColor.hex)
    const distance = colorDistance(inputRgb, dmcRgb)

    if (distance < minDistance) {
      minDistance = distance
      closestColor = dmcColor
    }

    // Perfect match, no need to continue
    if (distance === 0) {
      break
    }
  }

  return closestColor.hex
}

/**
 * Convert an array of colors to their closest DMC equivalents
 * @param {string[]} colors - Array of hex colors
 * @returns {string[]} Array of closest DMC hex colors
 */
export function mapToDMCColors(colors) {
  return colors.map(color => findClosestDMCColor(color))
}
